/**
 * Animation Timing Constants
 *
 * Centralized timing constants for consistent animation durations across the application
 */

/**
 * Fast animation duration (150ms)
 * Used for quick feedback, hover states, and immediate interactions
 */
export const FAST_DURATION = 150;

/**
 * Default animation duration (300ms)
 * Used for standard transitions and most animations
 */
export const DEFAULT_DURATION = 300;

/**
 * Slow animation duration (500ms)
 * Used for dramatic effects, page transitions, and emphasis
 */
export const SLOW_DURATION = 500;

/**
 * Stagger delay between items (100ms)
 * Used in stagger animations to create sequential appearance
 */
export const STAGGER_DELAY = 100;

/**
 * Page load animation total duration (1000ms)
 * Maximum time for all page load animations to complete
 */
export const PAGE_LOAD_DURATION = 1000;

/**
 * Form feedback delay (100ms)
 * Maximum time for form interaction feedback to appear
 */
export const FORM_FEEDBACK_DELAY = 100;
